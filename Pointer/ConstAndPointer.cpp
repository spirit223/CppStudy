#include<iostream>
using namespace std;


int main() {

	/* ------------常量指针-------------- */
	int a = 10;
	int b = 20;
	// 常量指针
	// 常量指针的指向可以改变, 但是指针指向的值不可以修改
	const int* p = &a;

	// 可以修改指针的指向
	p = &b;

	// *p = 30; 指针指向的变量的值不可以改变



	/* ------------指针常量-------------- */
	int i = 10, j = 20;
	int* const q = &i;

	// q = &j; 指针常量的指向不可以修改
	// 指针常量的值可以修改
	*q = 30;

	/* ------------常量指针常量-------------- */
	int x = 10, y = 20;
	const int* const poi = &x;

	// poi = &y;
	// *poi = 30

	return 0;
}